package be.ac.fundp.precise.dataManagment.hibernate.daos;

/**
 * The Enum ItemType represents an item of the UI in a hibernate DB..
 */
public enum ItemType {

	/** The bool. */
	bool,
	
	/** The hour. */
	hour,
	
	/** The date. */
	date,
	
	/** The natural. */
	natural,
	
	/** The integer. */
	integer,
	
	/** The real. */
	real,
	
	/** The text. */
	text,
	
	/** The array. */
	array,
	
	/** The multimedia. */
	multimedia;
}
